    function showSweetAlert(title, message, type) {
    const Toast = Swal.mixin({
        toast: true,
        position: 'top-end',
        showConfirmButton: false,
        timer: 10000,
        timerProgressBar: true,
        onOpen: (toast) => {
            toast.addEventListener('mouseenter', Swal.stopTimer)
            toast.addEventListener('mouseleave', Swal.resumeTimer)
        }
    });

    Toast.fire({
        icon: type,
        title: title,
        text: message,
        showClass: {
            popup: 'animate__animated animate__fadeInDown'
        },
        hideClass: {
            popup: 'animate__animated animate__fadeOutUp'
        },
        didOpen: () => {
            const audio = new Audio('{{ asset('sounds/notification.mp3') }}');
            audio.play();
        }
    });
}

  </script>


<script>
  function checkUpdates() {
    fetch('/api/check-updates')
        .then(response => {
            if (!response.ok) {
                throw new Error('Network response was not ok');
            }
            return response.json();
        })
        .then(data => {
            console.log("newCount: ", data.newCount);
            console.log("initialCount: ", data.initialCount);
            console.log("initialCount: ", data.count);
            if (data.count == 1) {
                showSweetAlert('New Cases', 'New cases have been added', 'success');
            }
        });
}


// call checkUpdates() initially
checkUpdates();

// call checkUpdates() every minute
setInterval(checkUpdates, 5000);

